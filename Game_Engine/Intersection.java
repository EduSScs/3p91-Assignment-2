package Game_Engine;
import java.util.*;

public class Intersection {

    public Intersection(double x, double y) {
        this.x = x;
        this.y = y;

    }
    // create an intersection with x and y coordinates
    private double x;
    private double y;

    // getters and setters
    public double getX() {
        return x;
    }
    public double getY() {
        return y;
    }



}
